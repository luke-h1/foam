export type IconName = keyof typeof icons;

export type IconWeight = 'regular' | 'bold' | 'fill' | 'duotone';

type Props = {
  color?: ThemeColor;
  name: IconName;
  size?: number;
  style?: StyleProp<ViewStyle>;
  weight?: IconWeight;
};

export function Icon({
  color,
  name,
  size = 24,
  style,
  weight = 'regular',
}: Props) {
  const Component = icons[name][weight];

  return <Component color={color} size={size} style={style} weight={weight} />;
}

type GetIconProps = {
  color: ThemeColor;
  name: IconName;
  size?: number;
  weight?: IconWeight;
};

export function GetIcon({
  color,
  name,
  size = 24,
  weight = 'duotone',
}: GetIconProps) {
  const { theme } = useUnistyles();
  return (
    <Icon color={theme.colors[color]} name={name} size={size} weight={weight} />
  );
}

