type EmojiCell = {
  emoji: string | SanitisiedEmoteSet;
  index: number;
};

export type ProcessedEmojiSection = {
  title: string;
  // eslint-disable-next-line @typescript-eslint/no-redundant-type-constituents
  icon: string | BrandIconName;
  data: EmojiCell[][];
  index: number;
  sectionOffset: number;
};

export function processEmojiSections(
  sections: EmojiSection[],
  chunkSize = 6,
): ProcessedEmojiSection[] {
  let globalIndex = 0;

  return sections.map((section, sectionIndex) => {
    const offset = globalIndex;

    // eslint-disable-next-line @typescript-eslint/ban-ts-comment
    // @ts-ignore
    const chunked = chunkArray(section.data, chunkSize).map(row =>
      row.map(emoji => ({
        emoji,
        index: (globalIndex += 1),
      })),
    );

    return {
      ...section,
      data: chunked,
      index: sectionIndex,
      sectionOffset: offset,
    };
  });
}

function chunkArray<T>(arr: T[], size: number): T[][] {
  const result: T[][] = [];
  for (let i = 0; i < arr.length; i += size) {
    result.push(arr.slice(i, i + size));
  }
  return result;
}

